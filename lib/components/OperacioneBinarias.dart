class OperacionesBinarias {
  double Sumar(double num1, double num2) {
    double Resul = 0;
    Resul = num1 + num2;

    return Resul;
  }

  double Restar(double num1, double num2) {
    double Resul = 0;
    Resul = num1 - num2;

    return Resul;
  }
  double Multiplicar(double num1, double num2) {
    double Resul = 0;
    Resul = num1 * num2;

    return Resul;
  }
}
